function [c, ceq, gc, gceq] = Three_circles(rx)
    x = rx(4:6);
    y = rx(7:9);

    c(1) = (rx(1) + rx(2))^2 - (x(1) - x(2))^2 - (y(1) - y(2))^2;
    c(2) = (rx(1) + rx(3))^2 - (x(1) - x(3))^2 - (y(1) - y(3))^2;
    c(3) = (rx(2) + rx(3))^2 - (x(2) - x(3))^2 - (y(2) - y(3))^2;

    ceq = [ ];

    if nargout > 2

        gc(1:9,1) = [2*rx(1) + 2*rx(2);
                     2*rx(1) + 2*rx(2);
                     0;
                     -2*x(1) + 2*x(2);
                     -2*x(2) + 2*x(1);
                     0;
                     -2*y(1) + 2*y(2);
                     -2*y(2) + 2*y(1)
                     0];

        gc(1:9,2) = [2*rx(1) + 2*rx(3);
                     0;
                     2*rx(1) + 2*rx(3);
                     -2*x(1) + 2*x(3);
                     0;
                     -2*x(3) + 2*x(1);
                     -2*y(1) + 2*y(3);
                     0;
                     -2*y(3) + 2*y(1)];

        gc(1:9,3) = [0;
                     2*rx(2) + 2*rx(3);
                     2*rx(2) + 2*rx(3);
                     0;
                     -2*x(2) + 2*x(3);
                     -2*x(3) + 2*x(2);
                     0;
                     -2*y(2) + 2*y(3);
                     -2*y(3) + 2*y(2)];

        gceq = [];
    end
end